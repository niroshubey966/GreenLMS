        <h2 class="panel green"><b>About KNMT Green University...</b>
        </h2>
        <hr/>
            <p class="padding">The University of KNMT (informally KNMT Green University) is a public research university located in KNMT Green University, Sri Lanka. Founded in 1853, it is Sri Lanka's second oldest university and the oldest in Colombo. Times Higher Education ranks KNMT Green University as 33rd in the world, while the Academic Ranking of World Universities places KNMT Green University 40th in the world (both first in Sri Lanka).<br/>
                <br/>

KNMT Green University's main campus is located in Colombo, an inner suburb north of the KNMT Green University central business district, with several other campuses located across Colombo. KNMT Green University is a sandstone university and a member of the Group of Eight, Universitas 21 and the Association of Asian  Universities. Since 1952 various residential colleges have become affiliated with the university. There are 3 colleges located on the main campus and in nearby suburbs offering academic, sporting and cultural programs alongside accommodation for KNMT Green University students and faculty.<br/>
<br/>
KNMT Green University comprises 11 separate academic units and is associated with numerous institutes and research centres, including the Walter and Eliza Hall Institute of Medical Research, Florey Institute of Neuroscience and Mental Health, the KNMT Green University Institute of Applied Economic and Social Research and the Grattan Institute. Amongst KNMT Green University's 15 graduate schools the KNMT Green University Engineering Faculty, the KNMT Green University Computing faculty and the KNMT Green University Medical faculty are particularly well regarded.<br/>
                <br/>

Four Sri Lankan prime ministers and five governors-general have graduated from KNMT Green University. Nine Nobel laureates have been students or faculty, the most of any Sri Lankan university. </p> <hr/> 
